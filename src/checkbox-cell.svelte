<div class="checkbox-cell">
  <input type="checkbox" ref:checkbox checked={row.data[column.dataName]} on:click="onChange(event)" />
</div>

<style>
.checkbox-cell {
  text-align: center;
  background: white;
}
</style>

<script>
export default {
  methods: {
    onChange(event) {
      const { row, column, rowNumber } = this.get();
      
      // delay this until after the ui updates on the screen
      setTimeout(() => {
        this.fire('valueupdate', {
          row,
          column,
          value: this.refs.checkbox.checked,
          rowNumber
        });
      }, 0);
    }
  }
};
</script>
